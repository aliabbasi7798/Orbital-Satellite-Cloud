def latlon_to_xyz(lat_deg, lon_deg, R=1.0):
    lat = np.deg2rad(lat_deg)
    lon = np.deg2rad(lon_deg)
    x = R * np.cos(lat) * np.cos(lon)
    y = R * np.cos(lat) * np.sin(lon)
    z = R * np.sin(lat)
    return x, y, z

