# =============== (D) Convert per-step edges to time intervals ===============
def edges_stepdict_from_snapshots(snapshots, topk_only=True):
    """
    Returns:
      times: sorted times (seconds)
      step_edges: list of dict { (src_node_id, dst_node_id, edge_type) : cap_bps } per step
    """
    times = [float(s["t_sec"]) for s in snapshots]
    step_edges = []

    for s in snapshots:
        E = {}

        # ISL edges from isl_caps: keys (i,j) already undirected with i<j
        for (i, j), cap in s["isl_caps"].items():
            key = (f"S_{i}", f"S_{j}", "ISL")
            E[key] = float(cap)

        # GSL edges from grid_topk: grid_id -> [(sat_id, cap_bps), ...]
        for gid, lst in s["grid_topk"].items():
            for (sid, cap) in lst:
                key = (f"G_{gid}", f"S_{sid}", "GSL")
                E[key] = float(cap)

        step_edges.append(E)

    return times, step_edges


def merge_edge_intervals(times, step_edges, step_duration_sec):
    """
    Merges consecutive steps where the same edge exists.
    Output rows have:
      src, dst, type, start_sec, end_sec, cap_mean, cap_min, cap_max, samples
    """
    active = {}  # key -> dict(start, last_t, caps[])
    intervals = []

    for idx, t in enumerate(times):
        cur = step_edges[idx]

        # Start/continue edges present now
        for key, cap in cur.items():
            if key in active:
                active[key]["last_t"] = t
                active[key]["caps"].append(cap)
            else:
                active[key] = {"start": t, "last_t": t, "caps": [cap]}

        # End edges not present now (compare against current set)
        cur_keys = set(cur.keys())
        for key in list(active.keys()):
            if key not in cur_keys:
                st = active[key]["start"]
                end = active[key]["last_t"] + step_duration_sec
                caps = np.array(active[key]["caps"], dtype=float)
                src, dst, etype = key
                intervals.append({
                    "src": src,
                    "dst": dst,
                    "type": etype,
                    "start_sec": float(st),
                    "end_sec": float(end),
                    "cap_mean_bps": float(caps.mean()),
                    "cap_min_bps": float(caps.min()),
                    "cap_max_bps": float(caps.max()),
                    "samples": int(len(caps)),
                })
                del active[key]

    # Close remaining active edges
    for key, info in active.items():
        st = info["start"]
        end = info["last_t"] + step_duration_sec
        caps = np.array(info["caps"], dtype=float)
        src, dst, etype = key
        intervals.append({
            "src": src,
            "dst": dst,
            "type": etype,
            "start_sec": float(st),
            "end_sec": float(end),
            "cap_mean_bps": float(caps.mean()),
            "cap_min_bps": float(caps.min()),
            "cap_max_bps": float(caps.max()),
            "samples": int(len(caps)),
        })

    return pd.DataFrame(intervals)


def export_dynamic_graph(snapshots, out_dir, step_duration_sec=3600):
    os.makedirs(out_dir, exist_ok=True)

    times, step_edges = edges_stepdict_from_snapshots(snapshots)
    intervals_df = merge_edge_intervals(times, step_edges, step_duration_sec)

    # Save both: per-step edges (optional) and merged intervals
    intervals_df.to_csv(os.path.join(out_dir, "edges_intervals.csv"), index=False)

    # Also save per-step edges as long-form (handy for debugging)
    rows = []
    for t, E in zip(times, step_edges):
        for (src, dst, etype), cap in E.items():
            rows.append({"t_sec": float(t), "src": src, "dst": dst, "type": etype, "cap_bps": float(cap)})
    step_df = pd.DataFrame(rows)
    step_df.to_csv(os.path.join(out_dir, "edges_per_step.csv"), index=False)

    return intervals_df, step_df


