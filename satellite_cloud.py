def add_population_from_geotiff(grids, meta, tif_path, band=1, assume_counts=True):
    """
    Adds columns:
      - population: sum of people in the grid cell
      - pop_share: population / global population

    Works best when tif values are "people per pixel" (counts).
    """
    dlat = meta["delta_lat_deg"]
    dlon = meta["delta_lon_deg"]
    n_lat = meta["n_lat"]
    n_lon = meta["n_lon"]
    n_cells = meta["n_cells"]

    pop = np.zeros(n_cells, dtype=np.float64)

    with rasterio.open(tif_path) as src:
        # Basic sanity checks
        if src.count < band:
            raise ValueError(f"Raster has {src.count} bands, but band={band} was requested.")

        if src.crs is None:
            raise ValueError("Raster CRS is missing. Need a WGS84 (EPSG:4326) raster.")
        # Many pop rasters are EPSG:4326; if not, reproject first (we can do that in the next step).
        # (Keeping this simple for now.)

        t = src.transform
        nodata = src.nodata

        # Iterate over raster blocks/windows (memory-safe)
        for _, window in src.block_windows(band):
            arr = src.read(band, window=window)

            # Mask invalid
            m = np.isfinite(arr)
            if nodata is not None:
                m &= (arr != nodata)
            if assume_counts:
                m &= (arr > 0)

            if not np.any(m):
                continue

            # Build lon/lat arrays for pixel centers in this window (vectorized affine math)
            # col indices: [col_off .. col_off+width-1], row indices similarly
            cols = (np.arange(window.col_off, window.col_off + window.width) + 0.5)
            rows = (np.arange(window.row_off, window.row_off + window.height) + 0.5)
            cc, rr = np.meshgrid(cols, rows)

            # Affine transform: x = c + a*col + b*row ; y = f + d*col + e*row
            lon = t.c + t.a * cc + t.b * rr
            lat = t.f + t.d * cc + t.e * rr

            vals = arr[m].astype(np.float64)
            latv = lat[m]
            lonv = lon[m]

            # Wrap/clip to stable ranges
            latv = np.clip(latv, -90.0 + 1e-12, 90.0 - 1e-12)
            lonv = ((lonv + 180.0) % 360.0) - 180.0

            # Map to your angular grid indices
            i = np.floor((latv + 90.0) / dlat).astype(np.int64)
            j = np.floor((lonv + 180.0) / dlon).astype(np.int64)
            i = np.clip(i, 0, n_lat - 1)
            j = np.clip(j, 0, n_lon - 1)

            gid = i * n_lon + j

            # Accumulate population into grid cells
            np.add.at(pop, gid, vals)

    grids2 = grids.copy()
    grids2["population"] = pop
    total = pop.sum()
    grids2["pop_share"] = grids2["population"] / total if total > 0 else 0.0
    return grids2

