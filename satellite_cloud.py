def analyze_link_changes(shell, inter_mode="one_per_neighbor",
                         n_steps=10, n_orbits=2):
    # times used before: n_steps over 0..2 orbits
    T_orbit = 2*np.pi / shell["n"]
    times = np.linspace(0, n_orbits*T_orbit, n_steps)

    # compute edge sets
    edge_sets = []
    for t in times:
        edges = build_links_one_timestep(shell, float(t), inter_mode=inter_mode)
        edge_sets.append(set(edges))

    # per-step stats
    per_step_m = [len(es) for es in edge_sets]

    stayed_per_transition = []
    new_per_transition = []
    dropped_per_transition = []

    for k in range(len(edge_sets) - 1):
        E0 = edge_sets[k]
        E1 = edge_sets[k+1]
        stayed = E0 & E1
        new = E1 - E0
        dropped = E0 - E1

        stayed_per_transition.append(len(stayed))
        new_per_transition.append(len(new))
        dropped_per_transition.append(len(dropped))

    # totals across all transitions (note: a link can be counted multiple times across transitions)
    total_stayed = sum(stayed_per_transition)
    total_new = sum(new_per_transition)
    total_dropped = sum(dropped_per_transition)

    # stability across the whole run
    stable_all_steps = set.intersection(*edge_sets) if edge_sets else set()
    ever_seen = set.union(*edge_sets) if edge_sets else set()

    print("=== Link change analysis ===")
    print(f"Steps: {n_steps} over {n_orbits} orbits (0..{n_orbits*T_orbit:.1f}s)")
    print(f"Inter-plane mode: {inter_mode}")
    print(f"Edges per step: min={min(per_step_m)}, max={max(per_step_m)}, avg={np.mean(per_step_m):.2f}")
    print()
    print("Per-transition (t_k -> t_{k+1}) totals:")
    print(f"  Stayed (intersection) total: {total_stayed}")
    print(f"  New (appeared) total:        {total_new}")
    print(f"  Dropped (disappeared) total: {total_dropped}")
    print()
    print("Whole-run stability:")
    print(f"  Links present in ALL steps:  {len(stable_all_steps)}")
    print(f"  Unique links EVER seen:      {len(ever_seen)}")
    print()

    # Return details if you want to inspect/plot later
    return {
        "times": times,
        "edges_per_step": per_step_m,
        "stayed_per_transition": stayed_per_transition,
        "new_per_transition": new_per_transition,
        "dropped_per_transition": dropped_per_transition,
        "stable_all_steps": stable_all_steps,
        "ever_seen": ever_seen,
        "edge_sets": edge_sets,
    }

