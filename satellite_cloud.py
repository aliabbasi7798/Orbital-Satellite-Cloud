def build_plane_index(shell):
    """
    Returns: dict plane -> list of sids in that plane ordered by s index.
    shell["sats"] items look like: (sid, p, s, raan, inc, a, n, M0)
    """
    by_plane = {}
    for (sid, p, s, raan, inc, a, n, M0) in shell["sats"]:
        by_plane.setdefault(p, []).append((s, sid))
    # sort by satellite index s within each plane
    for p in by_plane:
        by_plane[p].sort(key=lambda x: x[0])
        by_plane[p] = [sid for (s, sid) in by_plane[p]]
    return by_plane

def build_links_one_timestep(shell, t_sec, inter_mode="one_per_neighbor"):
    """
    inter_mode:
      - "one_per_neighbor": connect to the closest sat in plane p-1 and also in p+1 (2 links max)
      - "one_total": connect to the closest sat among BOTH neighboring planes (1 link max)
    Returns a list of undirected edges (i,j) with i<j.
    """
    sats = shell["sats"]
    N = len(sats)
    P = shell["P"]
    by_plane = build_plane_index(shell)

    # positions in ECEF (km)
    r_all = sat_positions_ecef(shell, t_sec)  # (N,3)

    edges = set()

    # --- Intra-plane: front & behind (ring)
    for p, sids in by_plane.items():
        S = len(sids)
        for idx, sid in enumerate(sids):
            sid_prev = sids[(idx - 1) % S]
            sid_next = sids[(idx + 1) % S]
            a, b = sorted((sid, sid_prev))
            edges.add((a, b))
            a, b = sorted((sid, sid_next))
            edges.add((a, b))

    # --- Inter-plane: closest in neighboring plane(s)
    for (sid, p, s, raan, inc, a, n, M0) in sats:
        neigh_planes = [ (p - 1) % P, (p + 1) % P ]
        candidates = []
        for pn in neigh_planes:
            for sid2 in by_plane[pn]:
                d = np.linalg.norm(r_all[sid] - r_all[sid2])
                candidates.append((d, pn, sid2))

        if not candidates:
            continue

        if inter_mode == "one_total":
            d, pn, best = min(candidates, key=lambda x: x[0])
            a, b = sorted((sid, best))
            edges.add((a, b))
        else:  # "one_per_neighbor"
            for pn in neigh_planes:
                cand_pn = [x for x in candidates if x[1] == pn]
                if cand_pn:
                    d, _, best = min(cand_pn, key=lambda x: x[0])
                    a, b = sorted((sid, best))
                    edges.add((a, b))

    return sorted(edges)

