def plot_ground_tracks(shell, num_planes=10, sats_per_plane=2, step_sec=60):
    import cartopy.crs as ccrs
    import cartopy.feature as cfeature
    import numpy as np
    import matplotlib.pyplot as plt

    def sat_pos_ecef_single(sat_tuple, t_sec):
        (sid, p, s, raan, inc, a, n_mean, M0) = sat_tuple
        nu = (M0 + n_mean * t_sec) % (2*np.pi)
        r_pqw = np.array([a*np.cos(nu), a*np.sin(nu), 0.0])
        r_eci = (R3(raan) @ (R1(inc) @ r_pqw))
        theta = OMEGA_EARTH * t_sec
        return R3(theta) @ r_eci

    def latlon_from_vec(r):
        x, y, z = r[0], r[1], r[2]
        lon = np.arctan2(y, x)
        hyp = np.sqrt(x*x + y*y)
        lat = np.arctan2(z, hyp)
        lon_deg = (np.rad2deg(lon) + 180) % 360 - 180
        lat_deg = np.rad2deg(lat)
        return lat_deg, lon_deg

    def split_on_dateline(lat, lon):
        lat = np.asarray(lat); lon = np.asarray(lon)
        jumps = np.where(np.abs(np.diff(lon)) > 180)[0]
        start = 0
        segs = []
        for j in jumps:
            segs.append((lat[start:j+1], lon[start:j+1]))
            start = j+1
        segs.append((lat[start:], lon[start:]))
        return segs

    # one orbit period
    T_orbit = 2*np.pi / shell["n"]
    times = np.arange(0, T_orbit + 1e-9, step_sec)

    fig = plt.figure(figsize=(14, 7))
    ax = plt.axes(projection=ccrs.PlateCarree())
    ax.set_global()
    ax.add_feature(cfeature.OCEAN, alpha=0.35)
    ax.add_feature(cfeature.LAND, alpha=0.6)
    ax.add_feature(cfeature.COASTLINE, linewidth=0.8)

    P, S = shell["P"], shell["S"]

    # choose exactly num_planes plane indices spread across [0, P-1]
    plane_ids = np.linspace(0, P-1, num_planes, dtype=int)

    for p in plane_ids:
        for k in range(sats_per_plane):
            s_idx = (k * max(1, S // sats_per_plane)) % S
            sid = p * S + s_idx
            sat_tuple = shell["sats"][sid]

            lat_list, lon_list = [], []
            for t in times:
                r = sat_pos_ecef_single(sat_tuple, t)
                lat, lon = latlon_from_vec(r)
                lat_list.append(lat); lon_list.append(lon)

            for lat_seg, lon_seg in split_on_dateline(lat_list, lon_list):
                ax.plot(lon_seg, lat_seg, transform=ccrs.PlateCarree(), linewidth=1)

    ax.set_title(f"Ground tracks over one orbit (num_planes={num_planes})")
    plt.show()

