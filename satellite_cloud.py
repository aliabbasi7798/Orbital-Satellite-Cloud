def plot_sats_over_n_orbits(shell, n_orbits=1, step_sec=1200, title=None):
    """
    Scatter sub-satellite points for ALL satellites over n_orbits.
    step_sec controls how often we sample positions (bigger = faster/less dense).
    """
    # One orbital period (seconds)
    T_orbit = 2*np.pi / shell["n"]

    times = np.arange(0, n_orbits * T_orbit + 1e-9, step_sec)

    lat_all = []
    lon_all = []

    for t in times:
        r = sat_positions_ecef(shell, t)      # (N,3) ECEF km
        lat, lon = ecef_to_latlon(r)          # (N,), (N,)
        lat_all.append(lat)
        lon_all.append(lon)

    lat_all = np.concatenate(lat_all)
    lon_all = np.concatenate(lon_all)

    if title is None:
        title = f"Sub-satellite points over {n_orbits} orbits (dt={step_sec}s)"

    plot_sats_2d_cartopy(lat_all, lon_all, title=title)
