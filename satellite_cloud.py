def run_10_steps_hourly_topk(shell, grids,
                            K=5,
                            inter_mode="one_per_neighbor",   # both neighbor planes
                            e_min_deg=10.0,
                            gsl_params=None,
                            isl_params=None,
                            ref_sid=0,
                            do_plots=True,
                            grid_chunk=256):
    """
    For each of 10 time steps (1 hour apart):
      - positions
      - ISL edges (in-plane +/-1 + closest in BOTH neighbor planes)
      - ISL capacities
      - For EACH grid: top-K visible satellites by GSL capacity (and those capacities)
    """

    if gsl_params is None:
        gsl_params = dict(f_hz=20e9, B_hz=250e6, Pt_dBW=10, Gt_dBi=35, Gr_dBi=25, NF_dB=3, other_losses_dB=2)
    if isl_params is None:
        isl_params = dict(f_hz=26e9, B_hz=1e9, Pt_dBW=10, Gt_dBi=30, Gr_dBi=30, NF_dB=3, other_losses_dB=2)

    # Precompute grid ECEF + normals
    grid_lat = grids["lat_c"].to_numpy()
    grid_lon = grids["lon_c"].to_numpy()
    grid_ids = grids["grid_id"].to_numpy()

    r_grids = latlon_to_ecef_km(grid_lat, grid_lon)              # (G,3)
    n_grids = r_grids / np.linalg.norm(r_grids, axis=1, keepdims=True)

    times = np.arange(0, 10*3600, 3600)  # 10 steps, 1 hour apart
    e_min_rad = np.deg2rad(e_min_deg)

    snapshots = []

    for step, t_sec in enumerate(times):
        # 1) sat positions
        r_sats = sat_positions_ecef(shell, float(t_sec))   # (N,3)
        N = r_sats.shape[0]
        G = r_grids.shape[0]

        # 2) ISL edges (your previous function)
        edges = build_links_one_timestep(shell, float(t_sec), inter_mode=inter_mode)

        # 3) ISL capacities
        isl_caps = {}
        for (i, j) in edges:
            d_km = float(np.linalg.norm(r_sats[i] - r_sats[j]))
            isl_caps[(i, j)] = float(shannon_capacity_bps_vec(d_km, **isl_params))

        # 4) For EACH grid: choose top-K visible satellites by GSL capacity
        grid_topk = {}  # grid_id -> list[(sat_id, cap_bps)] sorted high->low

        # chunk grids to avoid huge (G,N,3) allocations
        for start in range(0, G, grid_chunk):
            end = min(G, start + grid_chunk)
            rg = r_grids[start:end]          # (Gc,3)
            ng = n_grids[start:end]          # (Gc,3)
            gids = grid_ids[start:end]
            Gc = end - start

            # rho[g,s,:] = r_sats[s,:] - r_grid[g,:]
            rho = r_sats[None, :, :] - rg[:, None, :]             # (Gc,N,3)
            rho_norm = np.linalg.norm(rho, axis=2)                # (Gc,N)
            num = np.sum(rho * ng[:, None, :], axis=2)            # (Gc,N)
            elev = np.arcsin(np.clip(num / rho_norm, -1.0, 1.0))  # (Gc,N)

            vis = elev >= e_min_rad

            # capacity for all pairs in chunk, then mask
            caps = shannon_capacity_bps_vec(rho_norm, **gsl_params)
            caps[~vis] = -np.inf

            for ii in range(Gc):
                row = caps[ii]
                valid = np.isfinite(row)
                nv = int(valid.sum())
                if nv == 0:
                    continue

                if nv <= K:
                    idx = np.where(valid)[0]
                    idx = idx[np.argsort(row[idx])[::-1]]
                else:
                    idx = np.argpartition(row, -K)[-K:]
                    idx = idx[np.argsort(row[idx])[::-1]]

                grid_topk[int(gids[ii])] = [(int(sid), float(row[sid])) for sid in idx]

        if do_plots:
            plot_snapshot(shell, float(t_sec), edges, ref_sid=ref_sid)

        # quick summary
        total_gsl_links = sum(len(v) for v in grid_topk.values())
        avg_isl = (np.mean(list(isl_caps.values()))/1e9) if isl_caps else 0.0
        print(f"[{step+1}/10] t={t_sec:>5.0f}s | ISL edges={len(edges)} | avg ISL cap={avg_isl:.2f} Gbps | "
              f"grids w/ >=1 sat={len(grid_topk)} | total GSL links(top{K})={total_gsl_links}")

        snapshots.append({
            "t_sec": float(t_sec),
            "edges": edges,
            "isl_caps": isl_caps,     # (i,j) -> bps
            "grid_topk": grid_topk,   # grid_id -> [(sat_id, cap_bps), ...] length<=K
        })

    return snapshots

