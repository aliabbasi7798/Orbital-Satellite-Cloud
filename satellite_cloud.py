# --- constants ---
MU_EARTH = 398600.4418  # km^3 / s^2
R_EARTH = 6378.137      # km (WGS84 equatorial radius-ish)
OMEGA_EARTH = 7.2921150e-5  # rad/s

def R1(a):
    ca, sa = np.cos(a), np.sin(a)
    return np.array([[1,0,0],[0,ca,-sa],[0,sa,ca]])

def R3(a):
    ca, sa = np.cos(a), np.sin(a)
    return np.array([[ca,-sa,0],[sa, ca,0],[0,  0,1]])

def make_walker_shell(P=72, S=22, inc_deg=53.0, h_km=550.0, F=1):
    inc = np.deg2rad(inc_deg)
    a = R_EARTH + h_km
    n = np.sqrt(MU_EARTH / a**3)  # rad/s

    sats = []
    sat_id = 0
    for p in range(P):
        raan = 2*np.pi * p / P
        for s in range(S):
            M0 = 2*np.pi * (s / S) + 2*np.pi * (F * p) / (P * S)
            sats.append((sat_id, p, s, raan, inc, a, n, M0))
            sat_id += 1

    return {"P": P, "S": S, "N": P*S, "inc": inc, "a": a, "n": n, "sats": sats}

def sat_positions_ecef(shell, t_sec):
    a = shell["a"]
    positions = np.zeros((shell["N"], 3), dtype=np.float64)

    theta = OMEGA_EARTH * t_sec
    R_eci2ecef = R3(theta)

    for (sid, p, s, raan, inc, a, n, M0) in shell["sats"]:
        nu = (M0 + n * t_sec) % (2*np.pi)
        r_pqw = np.array([a*np.cos(nu), a*np.sin(nu), 0.0])

        r_eci = (R3(raan) @ (R1(inc) @ r_pqw))
        r_ecef = R_eci2ecef @ r_eci
        positions[sid] = r_ecef

    return positions

def ecef_to_latlon(r_ecef):
    x, y, z = r_ecef[:,0], r_ecef[:,1], r_ecef[:,2]
    lon = np.arctan2(y, x)
    hyp = np.sqrt(x*x + y*y)
    lat = np.arctan2(z, hyp)

    lat_deg = np.rad2deg(lat)
    lon_deg = np.rad2deg(lon)

    # optional: wrap to [-180, 180)
    lon_deg = (lon_deg + 180) % 360 - 180
    return lat_deg, lon_deg

