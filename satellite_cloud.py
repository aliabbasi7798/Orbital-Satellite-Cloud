import numpy as np
import pandas as pd

R_EARTH_KM = 6371.0

def build_angular_grid(delta_lat_deg=5.0, delta_lon_deg=5.0):
    # Edges
    lat_edges = np.arange(-90.0, 90.0 + delta_lat_deg, delta_lat_deg)
    lon_edges = np.arange(-180.0, 180.0 + delta_lon_deg, delta_lon_deg)

    n_lat = len(lat_edges) - 1
    n_lon = len(lon_edges) - 1

    rows = []
    grid_id = 0

    for i in range(n_lat):
        lat0, lat1 = lat_edges[i], lat_edges[i + 1]
        latc = 0.5 * (lat0 + lat1)

        # exact spherical area for this lat band + lon span
        lat0r = np.deg2rad(lat0)
        lat1r = np.deg2rad(lat1)

        for j in range(n_lon):
            lon0, lon1 = lon_edges[j], lon_edges[j + 1]
            lonc = 0.5 * (lon0 + lon1)

            dlon = np.deg2rad(lon1 - lon0)
            area_km2 = (R_EARTH_KM ** 2) * dlon * (np.sin(lat1r) - np.sin(lat0r))

            rows.append({
                "grid_id": grid_id,
                "i_lat": i,
                "j_lon": j,
                "lat_min": lat0,
                "lat_max": lat1,
                "lon_min": lon0,
                "lon_max": lon1,
                "lat_c": latc,
                "lon_c": lonc,
                "area_km2": float(area_km2),
            })
            grid_id += 1

    grids = pd.DataFrame(rows)

    meta = {
        "delta_lat_deg": delta_lat_deg,
        "delta_lon_deg": delta_lon_deg,
        "n_lat": n_lat,
        "n_lon": n_lon,
        "n_cells": int(n_lat * n_lon),
        "lat_edges": lat_edges,
        "lon_edges": lon_edges,
    }
    return grids, meta


def wrap_lon(lon):
    # wrap to [-180, 180)
    return ((lon + 180.0) % 360.0) - 180.0

def latlon_to_grid_id(lat, lon, meta):
    """
    Map a (lat, lon) to grid_id using meta from build_angular_grid.
    lat in [-90,90], lon any real.
    """
    lat = np.clip(lat, -90.0 + 1e-12, 90.0 - 1e-12)
    lon = wrap_lon(lon)

    lat_edges = meta["lat_edges"]
    lon_edges = meta["lon_edges"]
    n_lon = meta["n_lon"]

    i = np.searchsorted(lat_edges, lat, side="right") - 1
    j = np.searchsorted(lon_edges, lon, side="right") - 1

    # safety clamp
    i = int(np.clip(i, 0, meta["n_lat"] - 1))
    j = int(np.clip(j, 0, meta["n_lon"] - 1))

    return i * n_lon + j


