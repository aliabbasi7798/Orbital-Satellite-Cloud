# --- Vectorized Shannon capacity (works on arrays) ---
C_LIGHT = 299792458.0
K_BOLTZ = 1.380649e-23

def shannon_capacity_bps_vec(d_km, f_hz, B_hz,
                             Pt_dBW=10.0, Gt_dBi=35.0, Gr_dBi=25.0,
                             NF_dB=3.0, T_K=290.0, other_losses_dB=2.0):
    d_m = np.maximum(d_km, 1e-9) * 1000.0
    lam = C_LIGHT / f_hz
    L_fs = 20*np.log10(4*np.pi*d_m/lam)
    Pr_dBW = Pt_dBW + Gt_dBi + Gr_dBi - L_fs - other_losses_dB

    N_W = K_BOLTZ * T_K * B_hz
    N_dBW = 10*np.log10(N_W) + NF_dB

    snr_dB = Pr_dBW - N_dBW
    snr_lin = 10**(snr_dB/10.0)
    return B_hz * np.log2(1.0 + snr_lin)

def latlon_to_ecef_km(lat_deg, lon_deg, R_km=6378.137):
    lat = np.deg2rad(lat_deg)
    lon = np.deg2rad(lon_deg)
    x = R_km*np.cos(lat)*np.cos(lon)
    y = R_km*np.cos(lat)*np.sin(lon)
    z = R_km*np.sin(lat)
    return np.stack([x,y,z], axis=-1)

