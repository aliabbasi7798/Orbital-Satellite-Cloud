def add_population_from_worldpop_if_missing(grids, pop_tif, grid_res_deg=5.0):
    """
    If grids already has a 'population' column, does nothing.
    Otherwise: aggregates WorldPop raster into your existing grid centers (lat_c, lon_c).
    This matches the typical approach in your notebook.

    Requires: rasterio
    """
    if "population" in grids.columns:
        return grids

    import rasterio
    from rasterio.windows import Window

    g = grids.copy()

    # Build grid cell boundaries from centers (assumes regular grid spacing)
    half = grid_res_deg / 2.0
    # for each grid center, define bounding box [lat-half, lat+half] x [lon-half, lon+half]
    # We'll sample raster by converting bounds to pixel windows.

    with rasterio.open(pop_tif) as src:
        pop = src.read(1, masked=True)
        transform = src.transform

        def lonlat_to_rowcol(lon, lat):
            col, row = ~transform * (lon, lat)
            return int(np.floor(row)), int(np.floor(col))

        pops = []
        for lat_c, lon_c in zip(g["lat_c"].to_numpy(), g["lon_c"].to_numpy()):
            lat_min, lat_max = lat_c - half, lat_c + half
            lon_min, lon_max = lon_c - half, lon_c + half

            # Raster rows typically go from top (lat max) to bottom (lat min)
            r1, c1 = lonlat_to_rowcol(lon_min, lat_max)
            r2, c2 = lonlat_to_rowcol(lon_max, lat_min)

            rmin, rmax = sorted((r1, r2))
            cmin, cmax = sorted((c1, c2))

            # clip to raster bounds
            rmin = max(rmin, 0); cmin = max(cmin, 0)
            rmax = min(rmax, pop.shape[0]-1); cmax = min(cmax, pop.shape[1]-1)

            if rmin > rmax or cmin > cmax:
                pops.append(0.0)
                continue

            window = Window.from_slices((rmin, rmax+1), (cmin, cmax+1))
            block = src.read(1, window=window, masked=True)
            pops.append(float(block.filled(0).sum()))

    g["population"] = pops
    return g


